package com.trace.data;

public class Casino {
    private int id;
    private String clicianid;
    private String casinoname;
    private String log;
    private String lat;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getClicianid() {
        return clicianid;
    }

    public void setClicianid(String clicianid) {
        this.clicianid = clicianid;
    }

    public String getCasinoname() {
        return casinoname;
    }

    public void setCasinoname(String casinoname) {
        this.casinoname = casinoname;
    }

    public String getLog() {
        return log;
    }

    public void setLog(String log) {
        this.log = log;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }
}
